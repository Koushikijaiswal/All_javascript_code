//Create a basic calculator program using Javascript

let choise ='m';

switch(choise){
    case 'a':
        console.log(5+7);
        break;
    case 's':
        console.log(7-5);
        break;
    case 'm':
        console.log(7*5);
        break;
    case 'd':
        console.log(12/6);
        break;
    default:
        console.log("invalid choise");
}